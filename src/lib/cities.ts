/**
 * Локални (city) страници — единствен източник на истината.
 *
 * Данните оттук се използват на две места:
 *  1. React (city.tsx / LandingHero / CityLocal) — видимото съдържание;
 *  2. scripts/gen-city-pages.mjs — генерира <head> мета таговете, schema.org
 *     и sitemap.xml, за да не се разминават заглавията в HTML-а и в текста.
 *
 * Всеки град има собствен, неповтарящ се текст. Иначе Google третира
 * страниците като дублирано „doorway" съдържание и ги изхвърля от индекса.
 */

export type CityFaq = { q: string; a: string }

export type City = {
  /** URL сегмент: /varna, /sofia, ... */
  slug: string
  /** Именителен падеж — „Варна" */
  name: string
  /** С предлог — „във Варна", „в София" */
  inName: string
  /** <title> — до ~60 знака, за да не се реже в резултатите */
  title: string
  /** <meta name="description"> — до ~160 знака */
  description: string
  /** Три реда за H1; третият е в червено */
  heroLines: [string, string, string]
  heroSub: string
  /** Заглавие на локалната секция */
  localTitle: string
  /** Два уникални абзаца за местния пазар */
  localText: [string, string]
  /** Кратки локални акценти */
  bullets: string[]
  faq: CityFaq[]
  /** За LocalBusiness / Place schema */
  region: string
  postalCode: string
  geo: { lat: number; lng: number }
}

